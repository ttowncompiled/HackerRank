#!/bin/ruby

################################################################################
# There is an O(1) solution to this problem. The solution can be defined as
#
#   s = (1 + 2 + ... + N)**2 - (1**2 + 2**2 + ... + N**2), where
#       (1 + 2 + ... + N)**2 = [ N * (N+1) / 2 ]**2,
#       (1**2 + 2**2 + ... + N**2) = N * (N+1) * (2N+1) / 6,
#     = [ N * (N+1) / 2 ]**2 - N * (N+1) * (2N+1) / 6
#     = N * (N+1) * (3N+2) * (N-1) / 12
################################################################################

t = gets.strip.to_i
(1..t).each do
    n = gets.strip.to_i
    puts n * (n+1) * (3*n+2) * (n-1) / 12
end
